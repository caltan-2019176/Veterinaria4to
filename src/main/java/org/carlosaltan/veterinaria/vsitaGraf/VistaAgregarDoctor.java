    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        VistaDoctor doc = new VistaDoctor(); 
        try{
             String nombre = jTextField1.getText(); 
        String email = jTextField2.getText(); 
        String telefono = jTextField3.getText(); 
        String direccion = jTextField4.getText();
        String especialidad = jTextField5.getText(); 
        String colegiado = jTextField6.getText(); 
        boolean estado; 
        if (jCheckBox1.isSelected() == false){
           estado =  false;  
        }else {
            estado =  true;  
        }
        doc.nuevoDoctor(nombre, email, telefono, direccion, especialidad, colegiado, estado);
        this.setVisible(false);
        }catch (Exception e){
             JOptionPane.showMessageDialog(null, "ERROR", ":( ", JOptionPane.ERROR_MESSAGE);
             e.getMessage(); 
            
        }finally{
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField6.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

