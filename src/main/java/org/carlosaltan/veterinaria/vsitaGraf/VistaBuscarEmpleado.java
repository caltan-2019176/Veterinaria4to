    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        EmpleadoModelo empleado = new EmpleadoModelo(); 
        
        try{
            if (jTextField1.getText() == null){
           JOptionPane.showMessageDialog(null, "ERROR, VERIFIQUE LOS DATOS", ":( ", JOptionPane.ERROR_MESSAGE);
            }else{
            empleado = controlador.verEmpleadoPorCarne(jTextField1.getText()); 
            
            jTextField2.setText(empleado.getNombre());
            jTextField3.setText(empleado.getTelefono());
            jTextField4.setText(empleado.getEmail());
            jTextField5.setText(empleado.getTipo());
            jTextField6.setText(empleado.getArea());
            jTextField7.setText(empleado.getDireccion());
            jTextField8.setText(empleado.getCarne());
            
            
          }
       }catch(HeadlessException e){
            JOptionPane.showMessageDialog(null, "ERROR, VERIFIQUE LOS DATOS", ":( ", JOptionPane.ERROR_MESSAGE);
                e.getMessage(); 
       }finally{
            jTextField1.setText("");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

