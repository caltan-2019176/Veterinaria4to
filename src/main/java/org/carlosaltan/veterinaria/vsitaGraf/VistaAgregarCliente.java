    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        VistaCliente cliente = new VistaCliente(); 
        try{
            String nombre = jTextField1.getText(); 
            String telefono = jTextField2.getText(); 
            String email = jTextField3.getText(); 
            String idPersona = jTextField4.getText(); 
            int noMascotas = Integer.parseInt(jTextField5.getText()); 
            cliente.agregarCliente(nombre, telefono, email, idPersona, noMascotas);
            this.setVisible(false);
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "ERROR, VERIFIQUE LOS DATOS", ":( ", JOptionPane.ERROR_MESSAGE);
        }finally{
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
        }
        
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

