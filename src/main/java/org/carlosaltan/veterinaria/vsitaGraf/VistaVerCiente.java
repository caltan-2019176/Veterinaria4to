    public void mostrarDatos(){
        
        String [] encabezados = {"id", "Nombre", "Telefono", "Email", "DPI", "No.Mascotas"}; 
        int cantidadDeElementos = controlador.cantidadDeClientes(); 
        DefaultTableModel cabeceraDeTabla = new DefaultTableModel(encabezados, cantidadDeElementos);
        jTable1.setModel(cabeceraDeTabla);  
        TableModel listaDeDatos  = jTable1.getModel();  
        for (int i = 0; i< cantidadDeElementos; i++ ){
            ClienteModelo cliente = controlador.mostarCliente().get(i);
            listaDeDatos.setValueAt(cliente.getId(), i, 0); 
            listaDeDatos.setValueAt(cliente.getNombre(), i, 1);
            listaDeDatos.setValueAt(cliente.getTelefono(), i, 2); 
            listaDeDatos.setValueAt(cliente.getEmail(), i, 3);
            listaDeDatos.setValueAt(cliente.getIdPersonal(), i, 4);
            listaDeDatos.setValueAt(cliente.getNoMascotas(), i, 5);
   
        }
    }

