    public void mostrarDatos(){
        
        String [] encabezados = {"id", "Nombre", "Telefono", "Email", "Tipo", "Area", "Direccion", "Carne"}; 
        int cantidadDeElementos = controlador.cantidadDeEmpleados(); 
        DefaultTableModel cabeceraDeTabla = new DefaultTableModel(encabezados, cantidadDeElementos);
        jTable1.setModel(cabeceraDeTabla);  
        TableModel listaDeDatos  = jTable1.getModel();  
        for (int i = 0; i< cantidadDeElementos; i++ ){
            EmpleadoModelo empleado = controlador.mostarEmpleado().get(i);
            listaDeDatos.setValueAt(empleado.getId(), i, 0); 
            listaDeDatos.setValueAt(empleado.getNombre(), i, 1);
            listaDeDatos.setValueAt(empleado.getTelefono(), i, 2); 
            listaDeDatos.setValueAt(empleado.getEmail(), i, 3);
            listaDeDatos.setValueAt(empleado.getTipo(), i, 4);
            listaDeDatos.setValueAt(empleado.getArea(), i, 5);
            listaDeDatos.setValueAt(empleado.getDireccion(), i, 6);
            listaDeDatos.setValueAt(empleado.getCarne(), i, 7);
            
   
        }
    }

