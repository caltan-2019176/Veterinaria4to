     public void mostrarDatos(){
        
        String [] encabezados = {"id", "Nombre Persona", "DPI", "Fecha", "Hora", "Razon", "Cantidad de Mascotas"}; 
        int cantidadDeElementos = controlador.cantiadadDeCitas(); 
        DefaultTableModel cabeceraDeTabla = new DefaultTableModel(encabezados, cantidadDeElementos);
        jTable1.setModel(cabeceraDeTabla);  
        TableModel listaDeDatos  = jTable1.getModel();  
        for (int i = 0; i< cantidadDeElementos; i++ ){
            CitasModelo cliente = controlador.mostrarCita().get(i);
            listaDeDatos.setValueAt(cliente.getId(), i, 0); 
            listaDeDatos.setValueAt(cliente.getNombrePersona(), i, 1);
            listaDeDatos.setValueAt(cliente.getIdPersona(), i, 2); 
            listaDeDatos.setValueAt(cliente.getFecha(), i, 3);
            listaDeDatos.setValueAt(cliente.getHora(), i, 4);
            listaDeDatos.setValueAt(cliente.getRazon(), i, 5);
            listaDeDatos.setValueAt(cliente.getCantidadDemascotas(), i, 6);
            
   
        }
    }


