    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        CitasModelo cita = new CitasModelo(); 
        try{
            if (jTextField1.getText() == null){
           JOptionPane.showMessageDialog(null, "ERROR, VERIFIQUE LOS DATOS", ":( ", JOptionPane.ERROR_MESSAGE);
            }else{
            cita = controlador.verCitaId(jTextField1.getText()); 
            
            jTextField2.setText(cita.getNombrePersona());
            jTextField3.setText(cita.getIdPersona());
            jTextField4.setText(cita.getFecha());
            jTextField5.setText(cita.getHora());
            jTextField6.setText(cita.getRazon());
            jTextField7.setText(cita.getCantidadDemascotas());
            
          }
            
       }catch(HeadlessException e){
            JOptionPane.showMessageDialog(null, "ERROR, VERIFIQUE LOS DATOS", ":( ", JOptionPane.ERROR_MESSAGE);
                e.getMessage(); 
       }finally{
            jTextField1.setText("");
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

