        VistaCliente cliente = new VistaCliente(); 
        try{
            String idAnt = jTextField1.getText(); 
            String nombre = jTextField2.getText(); 
            String telefono = jTextField3.getText(); 
            String email = jTextField4.getText(); 
            String idPersona = jTextField5.getText(); 
            int noMascotas = Integer.parseInt(jTextField6.getText()); 
            cliente.actulizarCliente(idAnt, nombre, telefono, email, idPersona, noMascotas);
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "ERROR, VERIFIQUE LOS DATOS", ":( ", JOptionPane.ERROR_MESSAGE);
        }finally{
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");
        } 
